# data types in python

# What is a Data Type?
# A data type defines the kind of value a variable can store and what operations can be performed on that value.

# Why Do We Use Data Types?
# 1. To store different kinds of data correctly.
# 2. To perform appropriate operations on data.
# 3. To help Python understand how to handle the value.
# 4. To improve code readability and reliability.


