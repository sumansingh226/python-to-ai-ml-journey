# Rules for Variable Declaration in Python

# 1. Variable names must start with a letter (a-z, A-Z) or an underscore (_).
# 2. Variable names cannot start with a number.
# 3. Variable names can contain letters, numbers, and underscores only.
# 4. Special characters are not allowed (!, @, #, $, %, -, etc.).
# 5. Spaces are not allowed in variable names.
# 6. Variable names are case-sensitive (name, Name, and NAME are different).
# 7. Python keywords cannot be used as variable names (if, for, class, def, import, etc.).
# 8. Use meaningful and descriptive variable names.
# 9. Follow snake_case naming convention (student_name, total_marks).
# 10. Variables can be reassigned with new values at any time.
# 11. Python uses dynamic typing, so no type declaration is required.
# 12. Multiple variables can be assigned in a single line.
# 13. Multiple variables can share the same value.
# 14. Variable names should be readable and self-explanatory.
# 15. Avoid using built-in function names as variables (print, list, str, type, etc.).

